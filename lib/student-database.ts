export function isValidMatricula(matricula: string, city: string): boolean {
  const cityKey = city.toLowerCase().replace(" ", "-")
  const validList = validMatriculas[cityKey as keyof typeof validMatriculas]
  return validList ? validList.includes(matricula) : false
}

export function getCityFromMatricula(matricula: string): string | null {
  if (matricula.startsWith("101")) return "uberlandia"
  if (matricula.startsWith("202")) return "monte-carmelo"
  if (matricula.startsWith("303")) return "uberaba"
  return null
}

export function getStudentData(
  matricula: string,
): { name: string; rg: string; cpf: string; institution?: string; course?: string } | null {
  const city = getCityFromMatricula(matricula)
  if (!city) return null

  const cityData = studentDatabase[city as keyof typeof studentDatabase]
  return cityData?.[matricula] || null
}

export function getTransportCompany(matricula: string): string | null {
  const city = getCityFromMatricula(matricula)
  if (!city) return null

  const cityTransports = transportMapping[city as keyof typeof transportMapping]
  if (!cityTransports) return null

  for (const [company, matriculas] of Object.entries(cityTransports)) {
    if (matriculas.includes(matricula)) {
      return company
    }
  }

  return null
}

export function getAvailableTransportOptions(matricula: string): string[] {
  const transportCompany = getTransportCompany(matricula)
  if (transportCompany) {
    return [transportCompany]
  }

  const city = getCityFromMatricula(matricula)
  if (city === "uberlandia") {
    return [
      "Ônibus Municipal",
      "Ônibus Escolar",
      "Van Escolar",
      "Transporte Universitário",
      "Marques Turismo",
      "Silva&Cunha",
    ]
  } else if (city === "monte-carmelo") {
    return ["Ônibus Municipal", "Ônibus Escolar", "Van Escolar", "Transporte Universitário", "Novatur Ltda"]
  } else if (city === "uberaba") {
    return ["JN Tour (Arara)", "Cachoeira Transportes"]
  }

  return ["Ônibus Municipal", "Ônibus Escolar", "Van Escolar", "Transporte Universitário"]
}
